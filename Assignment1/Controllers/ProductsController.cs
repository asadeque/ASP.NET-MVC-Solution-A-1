using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using Assignment1.Models;

namespace Assignment1.Controllers
{
    public class ProductsController : Controller
    {
        private ProductDBContext db = new ProductDBContext();

        // GET: Products
        public ActionResult Index(string productGenre, string searchString)
        {
            var GenreLst = new List<string>();

            var GenreQry = from d in db.Products
                           orderby d.Genre
                           select d.Genre;

            GenreLst.AddRange(GenreQry.Distinct());
            ViewBag.productGenre = new SelectList(GenreLst);

            var products = from m in db.Products
                         select m;

            if (!String.IsNullOrEmpty(searchString))
            {
                products = products.Where(s => s.Title.Contains(searchString));
            }

            if (!string.IsNullOrEmpty(productGenre))
            {
                products = products.Where(x => x.Genre == productGenre);
            }

            return View(products);
        }

